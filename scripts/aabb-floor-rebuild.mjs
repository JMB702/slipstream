import { readFile, writeFile } from 'node:fs/promises';
import { FPS_SHOOTER_OBSTACLES } from '../packages/shared/src/maps/fps_shooter.collision.ts';

const FLOOR_TOP = 1.0;
const OLD_FLOOR_TOP_GUESS = 1.5;
const EPS = 0.05;

const FLOOR_BOX = { pos: [0, 0.6515, 0], halfSize: [15, 0.3485, 15] };

const fmt = (n) => {
  const s = n.toFixed(4).replace(/\.?0+$/, '');
  return s === '' || s === '-' ? '0' : s;
};

const isFloorStack = (o) => {
  const top = o.pos[1] + o.halfSize[1];
  return top <= OLD_FLOOR_TOP_GUESS + EPS;
};

const liftedToFloor = (o) => {
  const bot = o.pos[1] - o.halfSize[1];
  const top = o.pos[1] + o.halfSize[1];
  if (Math.abs(bot - OLD_FLOOR_TOP_GUESS) <= EPS) {
    const newBot = FLOOR_TOP;
    return {
      pos: [o.pos[0], (newBot + top) / 2, o.pos[2]],
      halfSize: [o.halfSize[0], (top - newBot) / 2, o.halfSize[2]],
    };
  }
  return o;
};

let dropped = 0;
let lowered = 0;
const kept = [];
for (const o of FPS_SHOOTER_OBSTACLES) {
  if (isFloorStack(o)) {
    dropped++;
    continue;
  }
  const adj = liftedToFloor(o);
  if (adj !== o) lowered++;
  kept.push(adj);
}
const finalList = [FLOOR_BOX, ...kept];

const lines = [
  '// AUTO-GENERATED by scripts/aabb-floor-rebuild.mjs — do not edit.',
  '// Source: previous voxelized fps_shooter.collision.ts, then:',
  `//   - dropped ${dropped} AABBs whose top ≤ ${OLD_FLOOR_TOP_GUESS} (the layered "floor stack")`,
  `//   - lowered the bottom of ${lowered} AABBs from ${OLD_FLOOR_TOP_GUESS} → ${FLOOR_TOP} (walls now reach the new floor)`,
  '//   - prepended the Floor Collider box from Blender as a single AABB',
  `// AABBs: ${finalList.length}`,
  "import type { Obstacle } from '../constants.js';",
  '',
  'export const FPS_SHOOTER_OBSTACLES: readonly Obstacle[] = [',
];
for (const o of finalList) {
  lines.push(
    `  { pos: [${fmt(o.pos[0])}, ${fmt(o.pos[1])}, ${fmt(o.pos[2])}], halfSize: [${fmt(o.halfSize[0])}, ${fmt(o.halfSize[1])}, ${fmt(o.halfSize[2])}] },`,
  );
}
lines.push('];');
lines.push('');

const src = await readFile(
  new URL('../packages/shared/src/maps/fps_shooter.collision.ts', import.meta.url),
  'utf8',
);
const boundsMatch = src.match(/export const FPS_SHOOTER_BOUNDS[\s\S]*$/);
if (boundsMatch) lines.push(boundsMatch[0]);

await writeFile(
  new URL('../packages/shared/src/maps/fps_shooter.collision.ts', import.meta.url),
  lines.join('\n'),
);

console.log(`Wrote ${finalList.length} AABBs (dropped ${dropped}, lowered ${lowered}, plus 1 floor box).`);
